export function standardizeTeamName(name: string): string {
  const trimmed = name.trim();
  if (TEAM_ALIASES[trimmed]) return TEAM_ALIASES[trimmed];
  return trimmed
    .replace(/\s+FC$/, "")
    .replace(/\s+AFC$/, "")
    .trim();
}
